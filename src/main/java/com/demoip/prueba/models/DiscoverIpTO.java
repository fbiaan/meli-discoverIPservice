package com.demoip.prueba.models;

public class DiscoverIpTO {
	private String ipSearch;
	private String fecha;
	private String pais;
	private String isoCode;
	private String idiomas;
	private String moneda;
	private String usoHorario;
	private Long distanciaBsAs;
	
	public DiscoverIpTO() {
		super();
	}
	
	public String getIpSearch() {
		return ipSearch;
	}
	public void setIpSearch(String ipSearch) {
		this.ipSearch = ipSearch;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	public String getIsoCode() {
		return isoCode;
	}
	public void setIsoCode(String isoCode) {
		this.isoCode = isoCode;
	}
	public String getIdiomas() {
		return idiomas;
	}
	public void setIdiomas(String idiomas) {
		this.idiomas = idiomas;
	}
	public String getMoneda() {
		return moneda;
	}
	public void setMoneda(String moneda) {
		this.moneda = moneda;
	}
	public String getUsoHorario() {
		return usoHorario;
	}
	public void setUsoHorario(String usoHorario) {
		this.usoHorario = usoHorario;
	}
	public Long getDistanciaBsAs() {
		return distanciaBsAs;
	}
	public void setDistanciaBsAs(Long distanciaBsAs) {
		this.distanciaBsAs = distanciaBsAs;
	}
	
	
	
}
